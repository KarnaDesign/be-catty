    <h2>NORWEGIAN FOREST CAT</h2>

    <p>The Norwegian Forest cat (Norwegian: Norsk skogkatt or Norsk skaukatt) is a breed of domestic cat originating
        in Northern Europe. This natural breed is adapted to a very cold climate, with top coat of glossy, long,
        water-shedding hairs, and a woolly undercoat for insulation.</p>
    <p>Although this is uncertain, the breed's ancestors may have been a landrace of short-haired cats brought to Norway
        by the Vikings around 1000 AD, who may also have brought with them long-haired cats, like those ancestral to the
        modern Siberian and Turkish Angora breeds.</p>
    <p>During World War II, the breed became nearly extinct until efforts by the Norwegian Forest Cat Club helped the
        breed by creating an official breeding program. It was registered as a breed with the European Fédération
        Internationale Féline in the 1970s, when a local cat fancier, Carl-Fredrik Nordane, took notice of the breed and
        made efforts to register it.</p>
    <p></p>Currently, the Norwegian Forest breed is very popular in Norway, Sweden, Iceland and France.</p>

