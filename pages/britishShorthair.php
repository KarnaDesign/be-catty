    <h2>BRITISH SHORTHAIR</h2>
    <p>The British Shorthair is the pedigreed version of the traditional British domestic cat, with a distinctively
        chunky body, dense coat and broad face. The most familiar colour variant is the "British Blue", a solid
        blue-gray with copper eyes, but the breed has also been developed in a wide range of other colours and
        patterns, including tabby and colorpoint.</p>
    <p> British Shorthair is one of the most ancient cat breeds known, probably originating from Egyptian domestic
        cats imported into Britain by the invading Romans in the first century AD. In modern times it remains the
        most popular
        pedigreed breed in its native country.</p>
    <p>The breed's good-natured appearance and relatively calm temperament make it a frequent media star, notably as
        the inspiration for Tenniel's famous illustration of the Cheshire Cat from Alice's Adventures in Wonderland.
        In the more modern era, a blue British Shorthair is the subject of the original "I Can Has Cheezburger?"
        image, credited with popularising the lolcat phenomenon.</p>

