    <h2>RAGDOLL</h2>
    <p>The Ragdoll is a cat breed with blue eyes and a distinct colorpoint coat. It is a large and muscular
        semi-longhair cat with a soft and silky coat. Developed by American breeder Ann Baker, it is best known for
        its docile and placid temperament and affectionate nature.</p>
    <p>The name "Ragdoll" is derived from the tendency of individuals from the original breeding stock to go limp and
        relaxed when picked up.</p>
    <p>Particularly popular in both the United Kingdom and the breed's native United States, ragdoll cats often are
        known as "dog-like cats" or "puppy-like cats" due to behaviors such as their tendency to follow people around,
        their ease at
        being physically handled, and their relative lack of aggression toward other pets.</p>

